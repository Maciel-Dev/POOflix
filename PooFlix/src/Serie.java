public class Serie {
    
    private int id;
    private String titulo;
    private int idade;

    public int getId() {
        return id;
    }

    public int getIdade() {
        return idade;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

}
